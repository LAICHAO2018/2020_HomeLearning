# 数学函数
1. ABS() 	# 绝对值函数
SELECT ABS(-2.4);

2. CEIL()	# 向上取整
SELECT CEIL(1.09);

3. FLOOR()	# 向下取整
SELECT FLOOR(1.009);

4. ROUND()	# 四舍五入
SELECT ROUND(1.88454);	
SELECT ROUND(1.88454,2);   # 参数2表示保留两位小数 

5. TRUNCATE() 	# 截断
SELECT TRUNCATE(1.88454,1);	# 强制取整

6. MOD() 	# 取余
SELECT MOD(-10,3);	# -10%3
SELECT MOD(10,1);	


